# ! 出力メソッドについて
# * pはダブルクォーテーションも出力する （主にデバックで使用する）
p "hello_world"

# * putsはクォーテーションの中身だけ出力する （基本はこれを使用する）
puts "hello"

# * printは自動的な改行をしない時に利用する
print "こんにちは"

# ! 命名規則について
# * スネークケースとは 英単語を英単語ごとに_で繋いで記述する
# ? (例） hello_world, hello_section

# * キャメルケースとは 英単語を英単語ごとに繋ぐ場合に最初の文字を大文字で記述する
# ? （例） helloWorld, helloSection

# * rubyでは基本的にスネークケースを使用する
# * クラスを定義するときだけキャメルケースを使用する

